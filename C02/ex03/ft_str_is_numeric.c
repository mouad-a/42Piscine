int	ft_str_is_numeric(char *str)
{
	int	ind;

	ind = 0;
	while (str[ind] != '\0')
	{
		if ((str[ind] < '0') || (str[ind] > '9'))
			return (0);
		ind++;
	}
	return (1);
}
