char	*ft_strlowcase(char *str)
{
	int	ind;

	ind = 0;
	while (str[ind] != '\0')
	{
		if ((str[ind] >= 'A') && (str[ind] <= 'Z'))
		{
			str[ind] += 32 ;
		}
		ind++;
	}
	return (str);
}
