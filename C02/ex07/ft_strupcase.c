char	*ft_strupcase(char *str)
{
	int	ind;

	ind = 0;
	while (str[ind] != '\0')
	{
		if ((str[ind] >= 'a') && (str[ind] <= 'z'))
		{
			str[ind] -= 32 ;
		}
		ind++;
	}
	return (str);
}
