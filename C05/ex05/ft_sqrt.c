#define MAX_INT 2147395600

int	ft_sqrt(int nb)
{
	int	i;

	if (nb < 1)
		return (0);
	if (nb > MAX_INT)
		return (0);
	i = 0;
	while (i * i <= nb)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
