int	ft_sum(int a, int b)
{
	return (a + b);
}

int	ft_min(int a, int b)
{
	return (a - b);
}

int	ft_mult(int a, int b)
{
	return (a * b);
}

int	ft_div(int a, int b)
{
	return (a / b);
}

int	ft_modu(int a, int b)
{
	return (a % b);
}
